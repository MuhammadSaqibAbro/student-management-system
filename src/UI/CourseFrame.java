     private void fillCourseTable() {
        defaultTableModel = new DefaultTableModel(columns, 0);
        List<Course> course = courseDAO.getAllCourse();

        for (Course c : course) {
            Object[] row = {c.getCrs_ID(), c.getCrs_name(), c.getCrs_code()};
            defaultTableModel.addRow(row);
            bTable.setModel(defaultTableModel);
        }
        bTable.getColumnModel().getColumn(0).setWidth(0);
        bTable.getColumnModel().getColumn(0).setMinWidth(0);
        bTable.getColumnModel().getColumn(0).setMaxWidth(0);
    }
