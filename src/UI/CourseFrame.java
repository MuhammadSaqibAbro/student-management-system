    private boolean validateFields() {
        if (bName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(CourseFrame.this, "Enter any Name");
            bName.grabFocus();
            return false;
        }
        if (bCode.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(CourseFrame.this, "Enter any Code");
            bCode.grabFocus();
            return false;
        }
        
   
        return true;
    }

